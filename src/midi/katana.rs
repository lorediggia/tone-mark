#[derive(Debug, Clone, Copy)]
pub struct Range {
    pub raw_min: u8,
    pub raw_max: u8,
    pub disp_min: i32,
    pub disp_max: i32,
    pub unit: &'static str,
}

pub fn raw_to_disp(r: &Range, raw: u8) -> i32 {
    if r.raw_max == r.raw_min {
        return r.disp_min;
    }
    let span_raw = r.raw_max as i32 - r.raw_min as i32;
    let span_disp = r.disp_max - r.disp_min;
    let v = (raw as i32 - r.raw_min as i32).max(0).min(span_raw);
    r.disp_min + (v * span_disp) / span_raw
}

pub fn format_value(addr: [u8; 4], raw: u8) -> String {
    if let Some(r) = param_range(addr) {
        let sep = if r.unit.is_empty() || r.unit.starts_with(' ') { "" } else { " " };
        format!("{}{}{}", raw_to_disp(&r, raw), sep, r.unit)
    } else {
        format!("0x{:02X}", raw)
    }
}

